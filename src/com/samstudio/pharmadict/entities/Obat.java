package com.samstudio.pharmadict.entities;

public class Obat {
	private String obat_id;
	private String obat_nama;
	private String obat_deskripsi;
	private String obat_indikasi;
	private String obat_harga;
	private String obat_jenis;
	private String obat_pic;
	
	public Obat(){
		
	}

	public String getObat_id() {
		return obat_id;
	}

	public void setObat_id(String obat_id) {
		this.obat_id = obat_id;
	}

	public String getObat_nama() {
		return obat_nama;
	}

	public void setObat_nama(String obat_nama) {
		this.obat_nama = obat_nama;
	}

	public String getObat_deskripsi() {
		return obat_deskripsi;
	}

	public void setObat_deskripsi(String obat_deskripsi) {
		this.obat_deskripsi = obat_deskripsi;
	}

	public String getObat_indikasi() {
		return obat_indikasi;
	}

	public void setObat_indikasi(String obat_indikasi) {
		this.obat_indikasi = obat_indikasi;
	}

	public String getObat_harga() {
		return obat_harga;
	}

	public void setObat_harga(String obat_harga) {
		this.obat_harga = obat_harga;
	}

	public String getObat_jenis() {
		return obat_jenis;
	}

	public void setObat_jenis(String obat_jenis) {
		this.obat_jenis = obat_jenis;
	}

	public String getObat_pic() {
		return obat_pic;
	}

	public void setObat_pic(String obat_pic) {
		this.obat_pic = obat_pic;
	}
}
